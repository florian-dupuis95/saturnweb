		<td><p>Le Système solaire (avec majuscule), ou système solaire (sans majuscule), est le système planétaire auquel appartient la Terre. Il est composé d'une étoile, le Soleil, et des objets célestes gravitant autour de lui : les huit planètes confirmées et leurs 205 satellites naturels connus (appelés usuellement des « lunes »), les cinq planètes naines et leurs neuf satellites naturels connus et les milliards de petits corps (la grande majorité des astéroïdes et autres planètes mineures, les comètes, les poussières interplanétaires, etc.). Le système solaire fait partie de la galaxie appelée Voie lactée. Il est situé à environ 8 kpc (∼26 100 a.l.) du centre galactique et effectue une révolution en 225 à 250 millions d'années.<br/><br/>
De façon schématique, le Système solaire est composé, outre le Soleil lui-même et par ordre de distance croissante à celui-ci, de quatre planètes telluriques internes (Mercure, Vénus, la Terre et Mars), d'une ceinture d'astéroïdes composée de petits corps rocheux, de quatre planètes géantes externes (deux géantes gazeuses que sont Jupiter et Saturne, et deux planètes géantes de glaces que sont Uranus et Neptune) et de la ceinture de Kuiper, composée elle-même d’objets glacés. L'héliopause, limite magnétique du Système solaire, est définie par l'arrêt des vents solaires face au vent galactique. Bien au-delà se trouve le nuage d'Oort, sphère d’objets épars. La limite gravitationnelle du Système solaire se situe bien plus loin encore, jusqu'à 1 ou 2 années-lumière du Soleil.<br/><br/>
Toutes les planètes du Système solaire à partir de la Terre possèdent des satellites en orbite, tandis que chacune des quatre planètes externes est en outre entourée d’un système d'anneaux de poussière et d’autres particules. Toutes les planètes, sauf la Terre, ont été nommées d'après des dieux et déesses de la mythologie romaine.<br/><br/>
Les cinq planètes naines, portant des noms de divinités diverses, sont Cérès, le premier et plus grand objet découvert dans la ceinture d’astéroïdes (975 km de diamètre équatorial), Pluton, le plus ancien et le plus grand objet connu de la ceinture de Kuiper (2 370 km), Éris, au diamètre très légèrement inférieur (2 326 km), qui se trouve dans le disque des objets épars, enfin Makémaké (environ 1 430 km) et Hauméa (1 960 km), des objets de la ceinture de Kuiper. Les quatre planètes naines transneptuniennes, c'est-à-dire orbitant au-delà de Neptune, sont de type « plutoïdes ».
