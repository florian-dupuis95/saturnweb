		<td><p>Dans la mythologie grecque orion est un chasseur légendaire qui se vantait de pouvoir tuer n'importe quel animal. Dans certaines versions de la légende, il fut tué par le Scorpion, qui a été placé à l'opposé de la voûte céleste par les dieux qui les ont ainsi séparés afin qu'ils ne soient jamais au-dessus de l'horizon en même temps. Sirius est son chien, ainsi que le nom de l'étoile principale de la constellation voisine du Grand Chien<br/><br/>
Orion fait partie des rares constellations immédiatement reconnaissables par leur forme. Ses sept étoiles les plus brillantes forment un nœud papillon (ou un sablier) facilement identifiable : quatre étoiles très brillantes forment un rectangle caractéristique au milieu duquel se trouve un alignement de trois autres étoiles, la ceinture d'Orion, qui constituent une signature remarquable.<br/><br/>
Le corps d'Orion est facilement visible, marqué par quatre étoiles brillantes qui sont (dans le sens des aiguilles d'une montre) Rigel (β Ori), Saïph (κ Ori), Bételgeuse (α Ori) et Bellatrix (γ Ori). Les deux plus brillantes, Rigel et Bételgeuse, occupent respectivement les coins Sud-Est et Nord-Ouest. Ces deux étoiles sont nettement colorées, Bételgeuse est rouge et Rigel est bleue.<br/><br/>
 Alnitak (ζ Ori), Mintaka (δ Ori) et Alnilam (ε Ori) (appelées également « les trois rois » ou « les trois mages ») constituent à elles trois la ceinture ou le baudrier d'Orion. Sous cet alignement, un autre alignement Nord-Sud, plus faible, marque l'épée d'Orion, qui se termine sur ι Ori. Un demi degré au nord de ι Ori, on trouve θ Ori, autour de laquelle est centrée la fameuse nébuleuse d'Orion, visible (faiblement, et par de bonnes conditions) à l'œil nu.
