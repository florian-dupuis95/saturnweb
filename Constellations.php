		<td><p>Citée par Aratos de Soles, puis par Ptolémée dans l'Almageste, la constellation représente Cassiopée, la reine d'Éthiopie de la mythologie grecque, femme de Céphée et mère d'Andromède, à côté desquels elle se trouve. Cette constellation fait partie du groupe de constellations rattachées au mythe d'Andromède. On dit aussi que, pour châtier son orgueil, la reine a été enchaînée à son trône, condamnée à tourner autour du pôle Nord et parfois de pendre à l'envers de façon très peu digne.<br/><br/>
La constellation se repère directement par sa forme très caractéristique en « M » ou « W » (suivant la saison). Ces étoiles bien brillantes (mag 2) restent longtemps visibles, et servent elles-mêmes à repérer d'autres constellations.<br/><br/>
Cassiopée permet de trouver l'étoile polaire, qui marque le pôle nord. Il n'y a guère besoin de prendre un alignement quelconque, l'étoile polaire (près du bord supérieur de l'illustration) est la seule étoile brillante située dans cette direction. En poursuivant dans le prolongement de cet axe, on tombe sur la constellation de la Grande Ourse, généralement basse sur l'horizon quand Cassiopée est bien visible. L'alignement du bord droit du « W », entre Caph (β Cas) et Shedar (α Cas), se prolonge vers les pieds d'Andromède, 51 And pour le pied Nord, et finalement Almach (γ And), son pied Sud. Dans l'autre sens, cet alignement conduit au milieu de la constellation de Céphée, le mari un peu insignifiant de Cassiopée. L'alignement interne du « W », entre Tsih (γ Cas) et Ruchbah (δ Cas), conduit à l'épaule de Persée, Mirphak (α Per), dans l'alignement formé par γ, α et δ Per.
