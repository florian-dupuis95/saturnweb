	<tr>
	<td><p>Cette constellation est associée à plusieurs oiseaux légendaires de la mythologie grecque :<br/>
•	Selon l'une des légendes, le dieu Zeus s'était déguisé en cygne pour séduire Léda, dont il eut pour enfants les Gémeaux et Hélène de Troie.<br/>
•	Elle pourrait également représenter Orphée, métamorphosé en cygne après son assassinat et placé dans les cieux à côté de sa lyre.<br/>
•	Enfin, on dit qu'un jeune homme nommé Cycnus était l'amant (ou le demi-frère) du malheureux Phaéton. Après que celui-ci a été foudroyé par Zeus pour avoir conduit les chevaux du Soleil et ainsi fait brûler la terre, Cycnus se mit à chercher son corps désespérément dans le fleuve Éridan où il était tombé. Cycnus plongea tant de fois dans le fleuve que Zeus eut pitié de lui et le changea en l'oiseau aquatique qui porte depuis son nom.<br/><br/>
L'étoile α Cygni (Deneb) marque l'un des angles du triangle d'été, visible dès que la nuit est tombée (mag 1). Des trois étoiles du triangle d'été, Deneb est la moins brillante. Quand les conditions de visibilité sont meilleures (mag 3), le cygne se reconnaît facilement par sa forme de croix très caractéristique. La « Croix du Nord » que dessine les étoiles principales du Cygne est très nette et très régulière ; elle sert de repère pour les constellations voisines. Le Cygne a un long cou et des ailes placées sur l'arrière, les deux figures sont donc inverses l'une de l'autre : la « tête » de la croix est donc la « queue » du Cygne, et la « tête » du Cygne forme le « pied » de la croix. L'étoile la plus brillante (mag 1) est Deneb, à la « tête » de la croix (et la base de la queue du Cygne). Les trois étoiles alignées du « bras » sont sensiblement de même éclat (mag 2), et sont d'est en ouest ε Cyg, γ Cyg (les épaules, dans l'axe du corps), et δ Cyg. Quand le bras commence à être visible, on prolonge le corps dans l'axe Deneb - γ Cyg, pour tomber 15° plus loin sur β Cyg (Albireo), qui marque le pied de la croix (et donc la tête du Cygne).<br/><br/> 
Le Petit Renard se situe immédiatement au sud du Cygne. Il borde toute la constellation, mais ses deux étoiles identifiables sont situées sous la tête du Cygne : ce sont les deux étoiles moyennes qui forment un alignement est-ouest, l'une à 5° de la tête vers le sud, et l'autre à 7° au sud-est (opposée à Véga). Au-delà du Petit Renard on reconnaît assez facilement la forme de la Flèche si la visibilité le permet. L'aile nord du Cygne pointe approximativement en direction de la tête du Dragon (suivant qu'on prend l'aile droite ou la gauche, on tombe au niveau de ξ Dra, la pointe du losange, ou au niveau des « deux yeux »). Cet alignement passe par le cœur du Dragon (η Dra), Thuban (α Dra) et le bord intérieur de la « casserole » (δ et γ UMa). Dans l'autre sens, l'aile du Cygne pointe sur une zone assez obscure où l'alignement passe par les pieds de Pégase, le centre du Verseau, et vient finir sur Fomalhaut après une traversée de près de 60°. Il n'y a pas d'alignement très convaincant dans l'axe du Cygne. La tête du Cygne pointe dans la direction de l'Aigle et du Serpentaire, tandis que la queue s'oriente vers Céphée.
