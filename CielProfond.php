		<td><p>La galaxie d'Andromède, également identifiée sous les numéros M31 dans le Catalogue de Messier et NGC 224, est une galaxie spirale située à environ 2,55 millions d'années-lumière du Soleil, dans la constellation d'Andromède.<br/><br/>
Appelée grande nébuleuse d’Andromède jusqu'à ce que sa vraie nature ait été reconnue dans les années 1920, la galaxie d'Andromède est la galaxie spirale la plus proche de la Voie lactée (toutes classes confondues, la galaxie la plus proche est la naine du Grand Chien) et le plus grand membre du Groupe local d'une soixantaine de galaxies individuelles dont toutes deux font partie. D'un diamètre d'environ 220 000 années-lumière, elle contiendrait environ mille milliards d'étoiles.<br/><br/>
Avec une magnitude visuelle de 3,4, la galaxie d'Andromède est l’une des rares galaxies observables à l'œil nu depuis la Terre dans l’hémisphère nord. C’est également un des objets les plus étendus de la voûte céleste, avec un diamètre apparent de 3,18°, soit plus de six fois le diamètre apparent de la Lune observée depuis la Terre.<br/><br/>
La première mention écrite connue de la galaxie d'Andromède remonte à 964, année durant laquelle elle est décrite par Abd al-Rahman al-Soufi dans son Livre des étoiles fixes. La première observation de la galaxie à l’aide d’un télescope est réalisée par Simon Marius en 1612 (souvent décrit comme le découvreur de la galaxie). Elle est photographiée pour la première fois en 1887 par l'astronome Isaac Roberts, dans son observatoire de Crowborough dans le Sussex.
