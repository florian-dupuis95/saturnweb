		<td><p>L'amas globulaire M13 ou Messier 13, très souvent appelé le Grand Amas d'Hercule, est parmi les objets les plus imposants du catalogue Messier. Il a été découvert par Edmond Halley en 1714, et ajouté par Charles Messier dans son catalogue le 1er juin 1764.<br/><br/>
Comportant plus de 500 000 étoiles, il est aussi l'un des plus vieux objets : son âge est estimé à 12 ou 14 milliards d'années. Il apparaît avec un diamètre de 20 minutes d'arc, soit un diamètre réel de 150 années-lumière. Il a cependant la particularité de contenir de nombreuses étoiles jeunes (appartenance confirmée d'après leur vitesse angulaire), ce qui est inhabituel pour un amas de cet âge : les scientifiques pensent que ces étoiles ne sont pas nées à l'intérieur de l'amas, mais ont plutôt été capturées par ce dernier.<br/><br/>
M13 a été utilisé en 1974 (le 16 novembre) comme cible pour l'envoi d'un message à une potentielle civilisation extraterrestre. Ce message a été envoyé à partir du radiotélescope d'Arecibo, qui participe également au projet SETI. Il contenait des informations comme les chiffres, le numéro atomique de l'hydrogène, du carbone, de l'azote, de l'oxygène et du phosphore, des données sur l'ADN, la forme et la taille d'un humain, la population terrestre, et la position de la Terre dans le système solaire. Il mettra plus de 22 000 ans à y parvenir (autant pour la réponse éventuelle).<br/><br/>
Avec une magnitude apparente de 5,8, un ciel sombre sans turbulences ni Lune brillante permet de le distinguer à l'œil nu. Avec des jumelles, on obtient visuellement une tache floue. Mais les chances d'observer les étoiles individuelles le constituant sont faibles. À partir de petits télescopes amateurs (114/900 par exemple), les étoiles de l'amas se découvrent. On peut observer dans son ensemble avec quelques nuances et détails.
Sa recherche dans le ciel est très facile. La constellation d'Hercule forme une figure de trapèze : si on se place pour avoir le grand côté en haut, l'amas se trouve sur le côté droit, à environ un tiers de la hauteur (en partant du coin supérieur droit).
