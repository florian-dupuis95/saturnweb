	<td><p>La nébuleuse de l'Œil de Chat (NGC 6543) est une nébuleuse planétaire située dans la constellation du Dragon, dont l'âge est estimé à 1 000 ans.<br/><br/>
Elle fut découverte par William Herschel le 15 février 1786, et c'est la première nébuleuse planétaire dont le spectre fut étudié, par l'astronome amateur anglais William Huggins en 1864. Elle est située à environ 1 104 pc (∼3 600 a.l.) de la Terre.<br/><br/>
Alors que la nébuleuse intérieure, avec un diamètre de 20 secondes d'arc, est plutôt petite, elle possède un halo de matière très étendu qui a été éjecté par l'étoile qui l'a engendré pendant sa phase de géante rouge. Le halo mesure 386 secondes d'arc (5,8 minutes d'arc).<br/><br/>
Selon une première interprétation, un système binaire serait à l'origine de la nébuleuse. Les effets dynamiques de deux étoiles tournant en orbite l'une autour de l'autre expliquent plus facilement la structure de cette nébuleuse, qui apparaît beaucoup plus complexe que la plupart des autres nébuleuses planétaires connues. Selon ce modèle, le vent stellaire de l'étoile centrale a créé l'enveloppe allongée, faite de gaz dense et lumineux. Cette structure est incluse à l'intérieur de deux lobes de gaz, éjectés plus tôt par cette étoile. Ces lobes sont « pincés » par un anneau de gaz plus dense, probablement éjecté dans le plan orbital du compagnon de l'étoile centrale.<br/><br/>
Sa déclinaison élevée signifie qu'elle est facilement observable de l'hémisphère Nord, où historiquement les télescopes les plus grands ont été situés. Elle est située presque exactement dans la direction du pôle Nord écliptique.
