		<td><p>En astrophysique, un trou noir est un objet céleste si compact que l'intensité de son champ gravitationnel empêche toute forme de matière ou de rayonnement de s’en échapper.<br/><br/>
De tels objets ne peuvent ni émettre, ni diffuser la lumière et sont donc noirs, ce qui en astronomie revient à dire qu'ils sont optiquement invisibles. Toutefois, plusieurs techniques d’observation indirecte dans différentes longueurs d'onde ont été mises au point et permettent d’étudier de nombreux phénomènes qu’ils induisent. En particulier, la matière happée par un trou noir est chauffée à des températures considérables avant d’être « engloutie » et émet une quantité importante de rayons X.<br/><br/>
Dans le cadre de la relativité générale, un trou noir est défini comme une singularité gravitationnelle occultée par un horizon absolu appelé horizon des évènements. Selon la physique quantique, un trou noir est susceptible de s'évaporer par l'émission d'un rayonnement de corps noir appelé rayonnement de Hawking.<br/><br/>
Un trou noir est un objet astrophysique dont la relativité générale dit qu'il est provoqué par une masse suffisamment concentrée pour qu'elle ne cesse de s'effondrer sur elle-même du fait de sa propre gravitation, arrivant même à se concentrer en un point appelé singularité gravitationnelle. Les effets de la concentration de cette masse permettent de définir une sphère, appelée l'horizon des évènements du trou noir, dont aucun rayonnement et a fortiori aucune matière ne peut s’échapper1,2. En effet, sa masse est telle, que même la lumière et ses photons ne peut échapper à son attraction gravitationnelle et parvenir à notre rétine (ou tout appareil d'observation). La vitesse de libération d'un trou noir n'étant pas atteignable par la lumière dont la vitesse est une constante physique, on convient qu'il est impossible d'échapper à l'attraction gravitationnelle d'un trou noir3. Cette sphère est centrée sur la singularité et son rayon ne dépend que de la masse centrale ; elle représente en quelque sorte l’extension spatiale du trou noir. À proximité de cette sphère, les effets gravitationnels sont observables et extrêmes.
