		<td><p>Le sens premier du mot étoile est celui d'un point lumineux dans le ciel nocturne, et par extension, des figures géométriques représentant des rayons partant d'un centre (voir le symbole de l'étoile). En astronomie, la signification scientifique plus restreinte d'étoile est celle d'un corps céleste plasmatique qui rayonne sa propre lumière par réactions de fusion nucléaire, ou des corps qui ont été dans cet état à un stade de leur cycle de vie, comme les naines blanches ou les étoiles à neutrons1. Cela signifie qu'ils doivent posséder une masse minimale pour que les conditions de température et de pression au sein de la région centrale — le cœur — permettent l'amorce et le maintien de ces réactions nucléaires, seuil en deçà duquel on parle d'objets substellaires. Les masses possibles des étoiles s'étendent de 0,085 masse solaire à une centaine de masses solaires. La masse détermine la température et la luminosité de l'étoile.<br/><br/>
La plupart des étoiles se situent sur la séquence principale du diagramme de Hertzsprung-Russell, où les étoiles produisent leur énergie et leur rayonnement par conversion de l'hydrogène en hélium, par des mécanismes de fusion nucléaire comme le cycle carbone-azote-oxygène ou la chaîne proton-proton.<br/><br/>
Pendant une grande partie de son existence, une étoile est en équilibre hydrostatique sous l'action de deux forces qui s'opposent : la gravitation, qui tend à contracter et faire s'effondrer l'étoile, et la pression cinétique (avec la pression de rayonnement pour les étoiles massives), régulée et maintenue par les réactions de fusion nucléaire, qui tend au contraire à dilater l'astre. À la fin de cette phase, marquée par la consommation de la totalité de l'hydrogène, les étoiles de la séquence principale se dilatent et évoluent en étoiles géantes, qui obtiennent leur énergie d'autres réactions nucléaires, comme la fusion de l'hélium en carbone et oxygène.<br/><br/>
Historiquement, les étoiles sont les points lumineux du ciel visibles uniquement la nuit et fixes les uns par rapport aux autres, par opposition aux planètes qui suivent des trajectoires errantes dans le ciel nocturne au cours de l'année. Les Anciens avaient une connaissance approfondie de la répartition des étoiles dans le ciel : ils les utilisaient pour la navigation et attribuaient des noms à certaines d'entre elles ainsi qu'aux formes qu'elles dessinent, les constellations. Cependant ils ignoraient tout de leur nature exacte, pensant souvent qu'il s'agissait d'orifices percés à travers la sphère céleste<br/><br/>
C'est seulement avec l'essor de l'astronomie moderne, c'est-à-dire de l'astrophysique, que les étoiles ont pu être comprises comme des objets de même nature que le Soleil mais situés à des distances considérablement plus grandes. Cette hypothèse fut énoncée pour la première fois par Giordano Bruno au XVIe siècle avant d'être confirmée expérimentalement en 1838 avec la première mesure de parallaxe réalisée par Friedrich Wilhelm Bessel, ainsi que par les observations spectrométriques effectuées grâce à l'appareil inventé en 1814 par l'opticien Joseph von Fraunhofer.<br/><br/>
Une étoile est un objet céleste en rotation, de forme a priori sphérique2, constitué essentiellement de plasma, et dont la structure est modelée par la gravité. Lors de sa formation, une étoile est essentiellement composée d’hydrogène et d’hélium. Durant la majeure partie de son existence, son cœur est le siège de réactions de fusion nucléaire, dont une partie de l’énergie est rayonnée sous forme de lumière ; la matière qui la compose s’en trouve presque complètement ionisée.
